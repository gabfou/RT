#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t n)
{
	size_t	dst_len;
	size_t	src_len;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (dst_len >= n)
		return (n + src_len);
	ft_strncat(dst + dst_len, src, n - dst_len - 1);
	return (dst_len + src_len);
}
