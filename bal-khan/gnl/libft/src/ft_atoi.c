#include "libft.h"

int				ft_atoi(const char *s)
{
	int ret;
	int sign;

	ret = 0;
	sign = 1;
	if (*s == '-' || *s == '+')
	{
		sign = -1;
		s++;
	}
	while (ft_isdigit(*s))
	{
		ret = (ret * 10) + (*s - '0');
		s++;
	}
	return (ret * sign);
}
